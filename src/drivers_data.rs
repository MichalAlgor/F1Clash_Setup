use crate::models::driver::DriverStats;

#[derive(Debug, Clone, Copy, PartialEq, Eq)]
pub enum DriverRarity {
    Common,
    Rare,
    Epic,
    Legendary,
    ProspectStandard,
    ProspectTurbocharged,
    PodiumStars,
    PodiumStarsLegends,
}

impl DriverRarity {
    pub fn label(&self) -> &'static str {
        match self {
            Self::Common => "Common",
            Self::Rare => "Rare",
            Self::Epic => "Epic",
            Self::Legendary => "Legendary",
            Self::ProspectStandard => "Prospect Standard",
            Self::ProspectTurbocharged => "Prospect Turbocharged",
            Self::PodiumStars => "Podium Stars",
            Self::PodiumStarsLegends => "Podium Stars Legends",
        }
    }

    pub fn css_class(&self) -> &'static str {
        match self {
            Self::Common => "rarity-common",
            Self::Rare => "rarity-rare",
            Self::Epic => "rarity-epic",
            Self::Legendary => "rarity-legendary",
            Self::ProspectStandard => "rarity-prospect-std",
            Self::ProspectTurbocharged => "rarity-prospect-turbo",
            Self::PodiumStars => "rarity-podium",
            Self::PodiumStarsLegends => "rarity-podium-legends",
        }
    }

    pub fn db_key(&self) -> &'static str {
        self.label()
    }

    pub fn from_db(s: &str) -> Option<DriverRarity> {
        match s {
            "Common" => Some(Self::Common),
            "Rare" => Some(Self::Rare),
            "Epic" => Some(Self::Epic),
            "Legendary" => Some(Self::Legendary),
            "Prospect Standard" => Some(Self::ProspectStandard),
            "Prospect Turbocharged" => Some(Self::ProspectTurbocharged),
            "Podium Stars" => Some(Self::PodiumStars),
            "Podium Stars Legends" => Some(Self::PodiumStarsLegends),
            _ => None,
        }
    }
}

#[derive(Debug, Clone, Copy, PartialEq, Eq)]
pub enum DriverCategory {
    Normal,
    Legendary,
    SpecialEdition,
}

impl DriverCategory {
    pub fn all() -> &'static [DriverCategory] {
        &[Self::Normal, Self::Legendary, Self::SpecialEdition]
    }
    pub fn display_name(&self) -> &'static str {
        match self {
            Self::Normal => "Normal",
            Self::Legendary => "Legendary",
            Self::SpecialEdition => "Special Edition",
        }
    }
}

impl DriverRarity {
    pub fn category(&self) -> DriverCategory {
        match self {
            Self::Common | Self::Rare | Self::Epic => DriverCategory::Normal,
            Self::Legendary => DriverCategory::Legendary,
            _ => DriverCategory::SpecialEdition,
        }
    }
}

#[derive(Debug, Clone)]
pub struct DriverDefinition {
    pub name: &'static str,
    pub rarity: DriverRarity,
    pub series: &'static str,
    pub levels: &'static [DriverLevelStats],
}

#[derive(Debug, Clone, Copy)]
pub struct DriverLevelStats {
    pub level: i32,
    pub overtaking: i32,
    pub defending: i32,
    pub qualifying: i32,
    pub race_start: i32,
    pub tyre_management: i32,
    pub cards_required: i32,
    pub coins_cost: i64,
    pub legacy_points: i32,
}

impl DriverLevelStats {
    const fn new(level: i32, ovt: i32, def: i32, qual: i32, rs: i32, tyre: i32, cards: i32, coins: i64, lp: i32) -> Self {
        Self { level, overtaking: ovt, defending: def, qualifying: qual, race_start: rs, tyre_management: tyre, cards_required: cards, coins_cost: coins, legacy_points: lp }
    }

    pub fn total(&self) -> i32 {
        self.overtaking + self.defending + self.qualifying + self.race_start + self.tyre_management
    }

    pub fn to_stats(&self) -> DriverStats {
        DriverStats { overtaking: self.overtaking, defending: self.defending, qualifying: self.qualifying, race_start: self.race_start, tyre_management: self.tyre_management }
    }
}

impl DriverDefinition {
    pub fn stats_for_level(&self, level: i32) -> Option<&DriverLevelStats> {
        self.levels.iter().find(|l| l.level == level)
    }
    pub fn max_level(&self) -> i32 {
        self.levels.last().map_or(1, |l| l.level)
    }
}

pub fn driver_catalog() -> &'static [DriverDefinition] { DRIVER_CATALOG }

pub fn find_driver(name: &str, rarity: &DriverRarity) -> Option<&'static DriverDefinition> {
    DRIVER_CATALOG.iter().find(|d| d.name == name && d.rarity == *rarity)
}

pub fn find_driver_by_db(name: &str, rarity_str: &str) -> Option<&'static DriverDefinition> {
    let rarity = DriverRarity::from_db(rarity_str)?;
    find_driver(name, &rarity)
}

pub fn drivers_by_category(category: DriverCategory) -> Vec<&'static DriverDefinition> {
    DRIVER_CATALOG.iter().filter(|d| d.rarity.category() == category).collect()
}

pub fn driver_catalog_index(name: &str, rarity: &DriverRarity) -> usize {
    DRIVER_CATALOG.iter().position(|d| d.name == name && d.rarity == *rarity).unwrap_or(usize::MAX)
}

